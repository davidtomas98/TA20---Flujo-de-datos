using System;

namespace Ejercicio8
{
    class Program
    {
        static void Main(string[] args)
        {
            // Recorrer números del 1 al 100 con un bucle while
            Console.WriteLine("Recorriendo números del 1 al 100 con un bucle while:");
            int i = 1;
            while (i <= 100)
            {
                Console.Write(i + " ");
                i++;
            }
        }
    }
}
