using System;

namespace Ejercicio7
{
    class Program
    {
        static void Main(string[] args)
        {
            // Recorrer números del 1 al 100 con un bucle for
            Console.WriteLine("Recorriendo números del 1 al 100 con un bucle for:");
            for (int i = 1; i <= 100; i++)
            {
                Console.Write(i + " ");
            }
        }
    }
}
