using System;

namespace Ejercicio18
{
    class Program
    {
        static void Main(string[] args)
        {
            // Mostrar los números del 1 al 100 con un bucle for
            Console.WriteLine("Números del 1 al 100 con un bucle for:");
            for (int i = 1; i <= 100; i++)
            {
                Console.Write(i + " ");
            }
        }
    }
}
